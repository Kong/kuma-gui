<script lang="ts" setup>
import CodeBlock from '@/app/common/code-block/CodeBlock.vue'
import ErrorBlock from '@/app/common/ErrorBlock.vue'
import LoadingBlock from '@/app/common/LoadingBlock.vue'
import type { ControlPlaneConfigSource } from '@/app/control-planes/sources'
</script>
<template>
  <RouteView
    v-slot="{ route, t }"
    name="diagnostics"
    :params="{
      codeSearch: '',
      codeFilter: false,
      codeRegExp: false,
    }"
  >
    <DataSource
      v-slot="{ data, error }: ControlPlaneConfigSource"
      :src="`/config`"
    >
      <AppView
        :breadcrumbs="[
          {
            to: {
              name: 'diagnostics',
            },
            text: t('diagnostics.routes.item.breadcrumbs'),
          },
        ]"
      >
        <template #title>
          <h1>
            <RouteTitle
              :title="t('diagnostics.routes.item.title')"
            />
          </h1>
        </template>

        <KCard>
          <ErrorBlock
            v-if="error"
            :error="error"
          />

          <LoadingBlock v-else-if="data === undefined" />

          <CodeBlock
            v-else
            data-testid="code-block-diagnostics"
            language="json"
            :code="JSON.stringify(data, null, 2)"
            is-searchable
            :query="route.params.codeSearch"
            :is-filter-mode="route.params.codeFilter"
            :is-reg-exp-mode="route.params.codeRegExp"
            @query-change="route.update({ codeSearch: $event })"
            @filter-mode-change="route.update({ codeFilter: $event })"
            @reg-exp-mode-change="route.update({ codeRegExp: $event })"
          />
        </KCard>
      </AppView>
    </DataSource>
  </RouteView>
</template>
