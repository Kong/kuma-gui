<template>
  <RouteView
    v-slot="{ route, t }"
    name="mesh-config-view"
    data-testid="mesh-config-view"
    :params="{
      mesh: '',
    }"
  >
    <AppView>
      <template #title>
        <h2>
          <RouteTitle
            :title="t('meshes.routes.item.navigation.mesh-config-view')"
          />
        </h2>
      </template>

      <KCard>
        <DataSource
          v-slot="{ data, error }: MeshSource"
          :src="`/meshes/${route.params.mesh}`"
        >
          <ErrorBlock
            v-if="error !== undefined"
            :error="error"
          />

          <LoadingBlock v-else-if="data === undefined" />

          <template v-else>
            <ResourceCodeBlock
              v-slot="{ copy, copying }"
              :resource="data.config"
            >
              <DataSource
                v-if="copying"
                :src="`/meshes/${route.params.mesh}/as/kubernetes?no-store`"
                @change="(data) => {
                  copy((resolve) => resolve(data))
                }"
                @error="(e) => {
                  copy((_resolve, reject) => reject(e))
                }"
              />
            </ResourceCodeBlock>
          </template>
        </DataSource>
      </KCard>
    </AppView>
  </RouteView>
</template>

<script lang="ts" setup>
import { MeshSource } from '../sources'
import ResourceCodeBlock from '@/app/common/code-block/ResourceCodeBlock.vue'
import ErrorBlock from '@/app/common/ErrorBlock.vue'
import LoadingBlock from '@/app/common/LoadingBlock.vue'
</script>
