<template>
  <div class="policy-list-content">
    <KCard
      class="policy-type-list"
      data-testid="policy-type-list"
    >
      <div
        v-for="(policyType, index) in visiblePolicyTypes"
        :key="index"
        class="policy-type-link-wrapper"
        :class="{
          'policy-type-link-wrapper--is-active': policyType.path === props.currentPolicyType.path,
        }"
      >
        <RouterLink
          class="policy-type-link"
          :to="{
            name: 'policy-list-view',
            params: {
              mesh: route.params.mesh,
              policyPath: policyType.path,
            },
          }"
          :data-testid="`policy-type-link-${policyType.name}`"
        >
          {{ policyType.name }}
        </RouterLink>

        <div class="policy-count">
          {{ props.meshInsight?.policies?.[policyType.name]?.total ?? 0 }}
        </div>
      </div>
    </KCard>

    <div class="policy-list">
      <div class="stack">
        <KCard>
          <div class="description">
            <div class="description-content">
              <h3>
                <PolicyTypeTag :policy-type="props.currentPolicyType.name">
                  {{ t('policies.collection.title', { name: props.currentPolicyType.name }) }}
                </PolicyTypeTag>
              </h3>

              <p>{{ t(`policies.type.${props.currentPolicyType.name}.description`, undefined, { defaultMessage: t('policies.collection.description') }) }}</p>
            </div>

            <div class="description-actions">
              <KBadge
                v-if="props.currentPolicyType.isExperimental"
                appearance="warning"
              >
                {{ t('policies.collection.beta') }}
              </KBadge>

              <KBadge
                v-if="props.currentPolicyType.isInbound"
                appearance="neutral"
              >
                {{ t('policies.collection.inbound') }}
              </KBadge>

              <KBadge
                v-if="props.currentPolicyType.isOutbound"
                appearance="neutral"
              >
                {{ t('policies.collection.outbound') }}
              </KBadge>

              <DocumentationLink
                :href="t('policies.href.docs', { name: props.currentPolicyType.name })"
                data-testid="policy-documentation-link"
              >
                <span class="visually-hidden">{{ t('common.documentation') }}</span>
              </DocumentationLink>
            </div>
          </div>
        </KCard>

        <KCard>
          <ErrorBlock
            v-if="props.policyError !== undefined"
            :error="props.policyError"
          />

          <AppCollection
            v-else
            class="policy-collection"
            data-testid="policy-collection"
            :empty-state-message="t('common.emptyState.message', { type: `${props.currentPolicyType.name} policies` })"
            :empty-state-cta-to="t('policies.href.docs', { name: props.currentPolicyType.name })"
            :empty-state-cta-text="t('common.documentation')"
            :headers="[
              { label: 'Name', key: 'name' },
              ...(props.currentPolicyType.isTargetRefBased ? [{ label: 'Zone', key: 'zone' }] : []),
              ...(props.currentPolicyType.isTargetRefBased ? [{ label: 'Target ref', key: 'targetRef' }] : []),
              { label: 'Details', key: 'details', hideLabel: true },
            ]"
            :page-number="props.pageNumber"
            :page-size="props.pageSize"
            :total="props.policyCollection?.total"
            :items="props.policyCollection?.items"
            :error="props.policyError"
            :is-selected-row="props.isSelectedRow"
            @change="emit('change', $event)"
          >
            <template #name="{ row }">
              <RouterLink
                :to="{
                  name: 'policy-summary-view',
                  params: {
                    mesh: row.mesh,
                    policyPath: props.currentPolicyType.path,
                    policy: row.name,
                  },
                  query: {
                    page: props.pageNumber,
                    size: props.pageSize,
                  },
                }"
              >
                {{ row.name }}
              </RouterLink>
            </template>

            <template #targetRef="{ row }">
              <template v-if="props.currentPolicyType.isTargetRefBased && typeof row.spec?.targetRef !== 'undefined'">
                <KBadge appearance="neutral">
                  {{ row.spec.targetRef.kind }}<span v-if="row.spec.targetRef.name">:<b>{{ row.spec.targetRef.name }}</b></span>
                </KBadge>
              </template>

              <template v-else>
                {{ t('common.detail.none') }}
              </template>
            </template>

            <template #zone="{ row }">
              <template v-if="row.labels && row.labels['kuma.io/origin'] === 'zone' && row.labels['kuma.io/zone']">
                <RouterLink
                  :to="{
                    name: 'zone-cp-detail-view',
                    params: {
                      zone: row.labels['kuma.io/zone'],
                    },
                  }"
                >
                  {{ row.labels['kuma.io/zone'] }}
                </RouterLink>
              </template>

              <template v-else>
                {{ t('common.detail.none') }}
              </template>
            </template>

            <template #details="{ row }">
              <RouterLink
                class="details-link"
                data-testid="details-link"
                :to="{
                  name: 'policy-detail-view',
                  params: {
                    mesh: row.mesh,
                    policyPath: props.currentPolicyType.path,
                    policy: row.name,
                  },
                }"
              >
                {{ t('common.collection.details_link') }}

                <ArrowRightIcon
                  display="inline-block"
                  decorative
                  :size="KUI_ICON_SIZE_30"
                />
              </RouterLink>
            </template>
          </AppCollection>
        </KCard>
      </div>
    </div>
  </div>
</template>

<script lang="ts" setup>
import { KUI_ICON_SIZE_30 } from '@kong/design-tokens'
import { ArrowRightIcon } from '@kong/icons'
import { computed } from 'vue'
import { useRoute } from 'vue-router'

import type { PolicyType } from '../data'
import { PolicyCollection } from '../sources'
import AppCollection from '@/app/application/components/app-collection/AppCollection.vue'
import DocumentationLink from '@/app/common/DocumentationLink.vue'
import ErrorBlock from '@/app/common/ErrorBlock.vue'
import PolicyTypeTag from '@/app/common/PolicyTypeTag.vue'
import type { MeshInsight } from '@/app/meshes/data'
import { useI18n } from '@/utilities'

type ChangeValue = {
  page?: number
  size?: number
}

const { t } = useI18n()
const route = useRoute()

const props = withDefaults(defineProps<{
  pageNumber: number
  pageSize: number
  policyTypes: PolicyType[]
  currentPolicyType: PolicyType
  policyCollection: PolicyCollection | undefined
  policyError: Error | undefined
  meshInsight: MeshInsight | undefined
  isSelectedRow?: ((row: any) => boolean) | null
}>(), {
  isSelectedRow: null,
})

const emit = defineEmits<{
  (event: 'change', value: ChangeValue): void
}>()

const visiblePolicyTypes = computed(() => {
  if (props.meshInsight?.policies === undefined) {
    return props.policyTypes
  }

  const hasAnyLegacyPolicies = props.policyTypes
    // Consider only legacy policy types. Ignore MeshGateway because we’ll always show it in the list. Therefore, its policies shouldn’t influence whether we show _legacy_ policy types.
    .filter((policyType) => !policyType.isTargetRefBased && policyType.name !== 'MeshGateway')
    .some((policyType) => (props.meshInsight?.policies?.[policyType.name]?.total ?? 0) > 0)

  return props.policyTypes.filter((policyType) => {
    if (policyType.isTargetRefBased || policyType.name === 'MeshGateway') {
      return true
    }

    // Show _all_ legacy policy types if _at least one_ has a policy
    return hasAnyLegacyPolicies
  })
})
</script>

